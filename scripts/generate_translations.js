// Assemble all translations
// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
const translations = { ar, fr, es, de, tr, fa, ur, zh, ja, ko, ru, pt, it, hi, id, nl };

// â”€â”€ Generate translated strings.xml files â”€â”€
for (const [langCode, langTranslations] of Object.entries(translations)) {
  const valuesDir = path.join(RES_DIR, `values-${langCode}`);
  if (!fs.existsSync(valuesDir)) {
    fs.mkdirSync(valuesDir, { recursive: true });
  }

  let xml = '<?xml version="1.0" encoding="utf-8"?>\n<resources>\n';

  // Always include app_name first
  if (langTranslations['app_name']) {
    xml += `    <string name="app_name">${escapeXml(langTranslations['app_name'])}</string>\n\n`;
  }

  for (const [text, { key }] of sortedEntries) {
    if (key === 'app_name') continue; // Already added
    if (langTranslations[key]) {
      xml += `    <string name="${key}">${escapeXml(langTranslations[key])}</string>\n`;
    }
  }

  xml += '</resources>\n';

  fs.writeFileSync(path.join(valuesDir, 'strings.xml'), xml, 'utf8');
  const translatedCount = Object.keys(langTranslations).length;
  console.log(`Written values-${langCode}/strings.xml (${translatedCount} translations)`);
}

